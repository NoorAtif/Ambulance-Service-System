    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Hospital_Name = evt.getActionCommand();
        Engine_Capacity = evt.getActionCommand();
        Model= evt.getActionCommand();
        Seat_Capacity=evt.getActionCommand();
         
         
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Hospital_Name = jButton2.getText();
        Engine_Capacity = jButton2.getText();
        Model = jButton2.getText();
        Seat_Capacity=jButton2.getText();
        model.addRow(new Object[]{Hospital_Name,Engine_Capacity , Model , Seat_Capacity});
        JOptionPane.showMessageDialog(null,"Data Added Successfully!");
         
    }//GEN-LAST:event_jButton2ActionPerformed

