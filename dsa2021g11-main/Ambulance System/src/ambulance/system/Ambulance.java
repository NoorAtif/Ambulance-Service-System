    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
        
        jTable1.setValueAt(Hospital_Name, jTable1.getSelectedRow(), 0);
        jTable1.setValueAt(Engine_Capacity, jTable1.getSelectedRow(), 1);
        jTable1.setValueAt(Model, jTable1.getSelectedRow(), 2);
        jTable1.setValueAt(Seat_Capacity, jTable1.getSelectedRow(), 3);

        
        JOptionPane.showMessageDialog(null," Now Update Accordingly!");
        }
        else{
        if(jTable1.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"Table is empty");
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select any row!");
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

