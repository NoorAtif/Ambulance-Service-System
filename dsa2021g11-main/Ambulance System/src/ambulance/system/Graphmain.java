    int getMinimumVertex(boolean [] set, int [] key)      	
	{
	       int minKey = Integer.MAX_VALUE;
	       int vertex = -1;
	       for (int i = 0; i <vertices ; i++)    
	       {
	           if(set[i]==false && minKey>key[i])       
	           {
	               minKey = key[i];
	               vertex = i;         
	           }
	       }
	       return vertex;
	}
    
    	void dijkstra_GetMinDistances(int sourceVertex)      	
	{
		   boolean[] place = new boolean[vertices];
		   int [] Dist = new int[vertices];
		   int Infinity = Integer.MAX_VALUE;
		   for (int i = 0; i <vertices ; i++)  
		   {
		    Dist[i] = Infinity; 
		   }
		   Dist[sourceVertex] = 0;
		   for (int i = 0; i <vertices ; i++)   
		   {
		
			   int U_Ver = getMinimumVertex(place, Dist);   
		       place[U_Ver] = true;                           		
		       for (int V_Ver = 0; V_Ver <vertices ; V_Ver++)
		       {
		    	   if(adjMat[U_Ver][V_Ver]>0)                    
		           {
		    		   if(place[V_Ver]==false && adjMat[U_Ver][V_Ver]!=Infinity) 
		               {
		    			   int newKey =  adjMat[U_Ver][V_Ver] + Dist[U_Ver];  
		                   if(newKey<Dist[V_Ver])  
		                   {
		                	   Dist[V_Ver] = newKey;        
		                   }
		               }
		           }
		       }
		   	}
		    printDijkstra(sourceVertex, Dist);   
	}
