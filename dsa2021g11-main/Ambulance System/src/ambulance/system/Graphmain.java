        void Search_Min(int []Dist)   //O(V^2)
	{
		Bubblesort(Dist);
		for (int i = 10; i <vertices ; i++)
		{
			for (int j = 10; j <vertices ; j++)
			{
				if(tempdistance[i]==Dist[j] && tempdistance[i-1]!=j-9)
					tempdistance[i]=j-9;
			}
		}
}
        void Bubblesort(int [] Dist)
	{
		int flag=0;
		int sort;
		for(int i=10;i<vertices;i++)  
		{
			tempdistance[i]=Dist[i];
		}
		for(int i=10;i<vertices-1;i++)    //bubble sort to sort the distances
		{
			flag=0;
			for(int j=10;j<vertices-1;j++)
			{
				if(tempdistance[j]>tempdistance[j+1])
				{
					sort=tempdistance[j];
					tempdistance[j]=tempdistance[j+1];
					tempdistance[j+1]=sort;
					flag=1;
				}
			}
			if(flag==0)
			{
				break;
			}
		}
		System.out.println();
	}

